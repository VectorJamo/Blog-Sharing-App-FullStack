const mongoose = require('mongoose')

const blog_schema = new mongoose.Schema({
    fname: {
        type: String,
        required: true
    },
    lname: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    }
})

const blog_model = mongoose.model('blog', blog_schema)

module.exports = blog_model